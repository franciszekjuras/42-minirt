#include "libft.h"

static int	ft_char(char c, char const *set)
{
	size_t	i;

	i = 0;
	while (set[i])
	{
		if (set[i] == c)
			return (1);
		i++;
	}
	return (0);
}

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*a;
	size_t	start;
	size_t	end;
	size_t	i;

	if (!s1 || !set)
		return (NULL);
	start = 0;
	end = ft_strlen(s1);
	i = 0;
	while (s1[start] && ft_char(s1[start], set))
		start++;
	while (end > start && ft_char(s1[end - 1], set))
		end--;
	a = (char *)malloc(sizeof(*s1) * (end - start + 1));
	if (!a)
		return (NULL);
	while (start < end)
		a[i++] = s1[start++];
	a[i] = 0;
	return (a);
}
